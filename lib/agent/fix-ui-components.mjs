  async fixDataViz() {
    const dataVizPath = join(this.projectRoot, 'components/ui/DataViz.tsx');
    
    if (!existsSync(dataVizPath)) {
      const dataVizContent = `import { cn } from "@/lib/utils";

interface DataVizProps extends React.HTMLAttributes<HTMLDivElement> {
  type: "chart" | "graph" | "metric" | "progress";
  data?: any;
  title?: string;
  description?: string;
}

export function DataViz({ 
  className, 
  type, 
  data, 
  title, 
  description, 
  ...props 
}: DataVizProps) {
  const renderContent = () => {
    switch (type) {
      case "metric":
        return (
          <div className="text-center">
            <div className="text-3xl font-bold text-accent mb-2">
              {data?.value || "0"}
            </div>
            <div className="text-sm text-muted-foreground">
              {data?.label || "Metric"}
            </div>
          </div>
        );
      
      case "progress":
        return (
          <div className="w-full">
            <div className="flex justify-between text-sm mb-2">
              <span>{data?.label || "Progress"}</span>
              <span>{data?.value || "0"}%</span>
            </div>
            <div className="w-full bg-panel2 rounded-full h-2">
              <div 
                className="bg-accent h-2 rounded-full transition-all duration-300"
                style={{ width: \`\${data?.value || 0}%\` }}
              />
            </div>
          </div>
        );
      
      case "chart":
        return (
          <div className="flex items-end justify-around h-32">
            {data?.values?.map((value: number, index: number) => (
              <div
                key={index}
                className="bg-accent rounded-t transition-all duration-300"
                style={{ 
                  height: \`\${(value / Math.max(...data.values)) * 100}%\`,
                  width: '20px'
                }}
              />
            )) || (
              <div className="text-muted-foreground">No data</div>
            )}
          </div>
        );
      
      default:
        return (
          <div className="text-muted-foreground">
            Data visualization placeholder
          </div>
        );
    }
  };

  return (
    <div
      className={cn(
        "bg-panel border border-border rounded-2xl p-6",
        className
      )}
      {...props}
    >
      {title && (
        <h3 className="text-lg font-semibold mb-2">{title}</h3>
      )}
      
      {description && (
        <p className="text-sm text-muted-foreground mb-4">{description}</p>
      )}
      
      {renderContent()}
    </div>
  );
}`;
      
      writeFileSync(dataVizPath, dataVizContent, 'utf8');
      this.fixes.files++;
    }
  }
